package funcionalidades;

import java.time.LocalDate;




/**
 * Esta classe cadastro é responsável por oferecer os métodos pros dados serem inseridos na tabela. (JTABLE1)
 * @author renat
 */

public class Cadastros {
   
    private LocalDate data;
    private float contaDeLuz;
    private float contaDeAgua;
    private float contaDeInternet;
    private float aluguel;
    private float gastosComComida;
    private float despesasComLazer;

    public LocalDate getData() {
        return data;
    }

    public void setData(LocalDate data) {
        this.data = data;
    }
    
    
    public float getContaDeLuz() {
        return contaDeLuz;
    }

    public void setContaDeLuz(float contaDeLuz) {
        this.contaDeLuz = contaDeLuz;
    }

    public float getContaDeAgua() {
        return contaDeAgua;
    }

    public void setContaDeAgua(float contaDeAgua) {
        this.contaDeAgua = contaDeAgua;
    }

    public float getContaDeInternet() {
        return contaDeInternet;
    }

    public void setContaDeInternet(float contaDeInternet) {
        this.contaDeInternet = contaDeInternet;
    }

    public float getAluguel() {
        return aluguel;
    }

    public void setAluguel(float aluguel) {
        this.aluguel = aluguel;
    }

    public float getGastosComComida() {
        return gastosComComida;
    }

    public void setGastosComComida(float gastosComComida) {
        this.gastosComComida = gastosComComida;
    }

    public float getDespesasComLazer() {
        return despesasComLazer;
    }

    public void setDespesasComLazer(float despesasComLazer) {
        this.despesasComLazer = despesasComLazer;
    }

   
    
    

   
    
    
    
    















}
